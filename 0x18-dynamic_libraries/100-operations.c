#include <stdio.h>

/**
 * add - My addition function
 * @a: first integer
 * @b: second integer
 *
 * Return: The return the value. @a + @b
*/
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - My subtraction function
 * @a: first integer
 * @b: second integer
 *
 * Return: The return the value. @a - @b
*/
int sub(int a, int b)
{
		return (a - b);
}

/**
 * mul- My multiplication function
 * @a: first integer
 * @b: second integer
 *
 * Return: The return the value. @a * @b
*/
int mul(int a, int b)
{
		return (a * b);
}

/**
 * div - My division function
 * @a: first integer
 * @b: second integer
 *
 * Return: The return the value. @a / @b
*/
int div(int a, int b)
{
		return (a / b);
}

/**
 * mod - My modulus function
 * @a: first integer
 * @b: second integer
 *
 * Return: The return the value. @a % @b
*/
int mod(int a, int b)
{
		return (a % b);
}
